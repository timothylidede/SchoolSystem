    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
            String sql = "SELECT * FROM tbl_student";   
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/136362_miniproject", "root", "");
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(sql);
            
            while(rs.next()){
                String studentUsername = rs.getString("student_username");
                String studentfName = rs.getString("student_fName");
                String studentlName = rs.getString("student_lName");
                String mathematics = String.valueOf(rs.getInt("mathematics"));
                String english = String.valueOf(rs.getInt("english"));
                String kiswahili = String.valueOf(rs.getInt("kiswahili"));
                
                String tbData[] = {studentUsername, studentfName, studentlName, mathematics, english, kiswahili};
                DefaultTableModel tblModel = (DefaultTableModel)jTable1.getModel();
                tblModel.addRow(tbData);
            }
            
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

